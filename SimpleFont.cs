    public static int CharWidth(int scale) => 5 * scale + scale;

    public static int CharHeight(int scale) => 7 * scale;

    public static int MeasureString(string text, int scale)
    {
        if (text.Length == 0)
        {
            return 0;
        }

        return text.Length * CharWidth(scale) - scale;
    }

    public static unsafe void DrawString(
        Sdl sdl,
        Renderer* renderer,
        string text,
        int x,
        int y,
        int scale,
        byte r,
        byte g,
        byte b)
    {
        var cursorX = x;
        foreach (var ch in text.ToUpperInvariant())
        {
            DrawChar(sdl, renderer, ch, cursorX, y, scale, r, g, b);
            cursorX += CharWidth(scale);
        }
    }

    public static unsafe void DrawChar(
        Sdl sdl,
        Renderer* renderer,
        char ch,
        int x,
        int y,
        int scale,
        byte r,
        byte g,
        byte b)
    {
        if (!Glyphs.TryGetValue(char.ToUpperInvariant(ch), out var rows))
        {
            return;
        }

        sdl.SetRenderDrawColor(renderer, r, g, b, 255);

        for (var row = 0; row < rows.Length; row++)
        {
            for (var col = 0; col < rows[row].Length; col++)
            {
                if (rows[row][col] != '1')
                {
                    continue;
                }

                var rect = new Rectangle<int>(
                    x + col * scale,
                    y + row * scale,
                    scale,
                    scale);
                sdl.RenderFillRect(renderer, ref rect);
            }
        }
    }
